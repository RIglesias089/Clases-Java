package org.modelo.fundamentos;

public class While {
    public static void main(String[] args){
        //El while es un bucle que se repetira siempre y cuando algo sea verdadero
        int i = 1;

        while (i <= 5) {
            System.out.println(i);
            i++;
        }
    //Se siferencia del for porque se usa cuando no sabes cuantas veces repetir algo
    }

}

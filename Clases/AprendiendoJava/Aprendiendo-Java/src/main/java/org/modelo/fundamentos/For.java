package org.modelo.fundamentos;

public class For {
    /*El ciclo for es muy importante ya que con este podemos repetir el codigo
    * varias veces, cuenta con una sintaxis un poquito copleja pero que se ve de la siguiente manera:
    *
    * for(inicio; condicion(cuando esta sea verdad y se cumpla); incremento){}
    *
    * Usado cundo sabes cuantas veces debes repetir algo
    * */

    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }

}
